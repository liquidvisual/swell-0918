$(".global-footer .page-state-switcher input").on("click", function (event) {
    var $body = $("body");
    var $this = $(this);
    var val = $this.attr("value");

    if ($this.is(":checked")) {
        $body.addClass(val);
    } else {
        $body.removeClass(val);
    }

    setTimeout(function () {
        setHeadroomOffset();
    }, 1000);
});

